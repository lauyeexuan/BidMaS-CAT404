          checkProjectBids(
            schoolId, 
            academicYearId, 
            project.major, 
            majorDocMap.get(project.major), 
            project.id, 
            projectsMap
          )
        })
        
        // Schedule next batch
        currentBatch++
        if (startIdx + batchSize < projectsToCheck.length) {
          setTimeout(processBatch, 300)
        }
      }
      
      // Start processing batches
      processBatch()
    }, 500)
    
    // Call fetchExaminedProjects if user is a lecturer
